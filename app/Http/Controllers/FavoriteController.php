    public function ajaxlike(Request $request)
    {
        $id = Auth::user()->id;
        $sweet_id = $request->sweet_id;
        $like = new Favolite;
        $sweet = Sweet::findOrFail($sweet_id);

        // 空でない（既にいいねしている）なら
        if ($like->like_exist($id, $sweet_id)) {
            //likesテーブルのレコードを削除
            $like = Favolite::where('sweet_id', $sweet_id)->where('user_id', $id)->delete();
        } else {
            //空（まだ「いいね」していない）ならlikesテーブルに新しいレコードを作成する
            $like = new Favolite;
            $like->sweet_id = $request->sweet_id;
            $like->user_id = Auth::user()->id;
            $like->save();
        }

        //loadCountとすればリレーションの数を○○_countという形で取得できる（今回の場合はいいねの総数）
        $sweetLikesCount = $sweet->loadCount('favolits')->favolits_count;

        //一つの変数にajaxに渡す値をまとめる
        //今回ぐらい少ない時は別にまとめなくてもいいけど一応。笑
        $json = [
            'sweetLikesCount' => $sweetLikesCount,
        ];
        //下記の記述でajaxに引数の値を返す
        return response()->json($json);
    }
